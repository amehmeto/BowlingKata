import { Bowling } from "./Bowling"

describe('Bowling kata', () => {
    it.each([
        ['--------------------', 0],
        ['1-------------------', 1],
        ['-------------------1', 1],
        ['33-------444-------1', 19],
        ['9-9-9-9-9-9-9-9-9-9-', 90],
        ['X-------------------', 10],
        ['X-1-----------------', 11],
        ['9/------------------', 10],
        ['4/------------------', 10],
        ['4/2-----------------', 14],
        ['4/7-----------------', 24],
        ['4/72----------------', 26],
        ['4/7/----------------', 27],
        ['4/7/4/--------------', 41],
        ['4/7/4/-1------------', 42],
    ])('should return %i when %s', (rawScore: string, computedScore: number) => {
        expect(new Bowling().computeScore(rawScore)).toBe(computedScore)
    })
})